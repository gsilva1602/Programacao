numeros = [20]
qnt = int(input('Digite quantos vetores deseja (máximo 20): '))
while(qnt>20):
    print("Erro! Máximo de 20 vetores, tente novamente")
    qnt = int(input('Digite quantos vetores deseja (máximo 20): '))
for i in range (0, qnt, 1):
    x = int(input('Digite os vetores: '))
    numeros.append(x)
for i in range (0, i<=0, 1):
    x = int(input('Agora digite o vetor para saber a sua posição: '))
    numeros.append(x)
